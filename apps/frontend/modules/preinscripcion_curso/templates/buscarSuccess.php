                                <strong>REQUISITOS</strong> <br>
                                •	Ser bachiller en cualquiera de sus modalidades.<br>
                                •	Vocación para el ejercicio de profesiones vinculadas al ámbito de la salud <br>
                                •	Dedicación exclusiva al estudio (sólo para el PNF-MIC)<br>
                                •	Disposición para ser ubicado durante su formación en las Áreas de Salud Integral Comunitaria (ASIC)  priorizadas por municipio o estado. <br>
