                                •	Postulación del Comité de Salud o Consejo Comunal de su lugar de residencia.  <br>
                                <br><br>

                                <strong>RECAUDOS</strong> <br>

                                •	Una carpeta marrón tipo oficio con gancho.<br>
                                •	Dos fotos tamaño carné, fondo blanco.<br>
                                •	Una  (01) fotocopia de la cédula de identidad (ampliada 300 x 300).<br>
                                •	Una (01) Fotocopia de la Partida de Nacimiento, sin enmiendas y/o dañada, con vista al original.<br>
                                •	Una (01) fotocopia simple del título de bachiller con sus respectivos timbres fiscales. (con vista al original).  En los casos de los bachilleres 2018, se solicitará la Constancia de Culminación de estudios, emitida por el Liceo de procedencia. Al finalizar el Curso Introductorio, deben presentar obligatoriamente sus documentos probatorios de bachillerato (título y notas).<br>
                                •	Una (01) fotocopia simple de las Notas certificadas de 1ro., a 5to., año de bachillerato, respectivamente certificadas por  el plantel donde cursó los estudios o por la Zona Educativa del Estado donde los culminó, en los casos que lo ameriten, con sus respectivos timbres fiscales (con vista al original).<br>
                                •	Una (01) Fotocopia del Certificado y/o Constancia de Registro en el Sistema Nacional de Ingreso (SNI) a la Educación Universitaria. <br>
                                •	En el caso de aspirantes de otras nacionalidades: presentar la cédula de identidad que los acredite como Residentes en Venezuela (original y fotocopia ampliada).<br>
                                •	Los aspirantes nacionalizados deben presentar copia de la Gaceta Oficial con la Resolución de Nacionalización.<br>
                                •	Los aspirantes, ciudadanos venezolanos o de otras nacionalidades, que hayan realizado estudios de bachillerato fuera del territorio venezolano, deberán realizar previamente la legalización de los mismos ante las autoridades correspondientes, de acuerdo a los tratados y convenios firmados por el Estado venezolano.<br>
