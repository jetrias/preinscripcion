                                <strong>REQUISITOS</strong> <br>
                                •	Ser bachiller en cualquiera de sus modalidades.<br>
                                •	Vocación para el ejercicio de profesiones vinculadas al ámbito de la salud <br>
                                •	Dedicación exclusiva al estudio (sólo para el PNF-MIC)<br>
                                •	Disposición para ser ubicado durante su formación en las Áreas de Salud Integral Comunitaria (ASIC)  priorizadas por municipio o estado. <br>
                                •	Edad no mayor de 35 años (preferiblemente).<br>
                                •	Postulación del Comité de Salud o Consejo Comunal de su lugar de residencia.  <br>
                                <br><br>

                                <strong>RECAUDOS</strong> <br>

                                •	Una carpeta marrón tipo oficio con gancho.<br>
                                •	Dos (02) fotos tamaño carné, fondo blanco.<br>
                                •	Una (01) fotocopia de la cédula de identidad (ampliada 300 x 300)en una hoja sin recortar.<br>
                                •	Original y una (01) Fotocopia de la Partida de Nacimiento, sin enmiendas y/o dañada, con vista al original.<br>
                                •	Original y una (01) fotocopia simple del título de bachiller con sus respectivos timbres fiscales. (con vista al original).  En los casos de los bachilleres 2018, se solicitará la Constancia de Culminación de estudios, emitida por el Liceo de procedencia. Al finalizar el Curso Introductorio, deben presentar obligatoriamente sus documentos probatorios de bachillerato (título y notas).<br>
                                •	Original y una (01) fotocopia simple de las Notas certificadas de 1ro., a 5to., año de bachillerato, respectivamente certificadas por  el plantel donde cursó los estudios o por la Zona Educativa del Estado donde los culminó, en los casos que lo ameriten, con sus respectivos timbres fiscales (con vista al original).<br>
                                        NOTA: A los bachilleres egresados del 2020, se les solicitará la  Constancia de Culminación de Estudios emitida por el Liceo  de procedencia, cuando no se les haya emitido su respectivo título de bachiller ni las Notas Certificadas. Al finalizar el curso deberán presentar obligatoriamente sus documentos probatorios de bachillerato (título y notas certificadas).
                                •	Certificado de Registro en el Sistema Nacional de Ingreso (SNI) a la Educación Universitaria.   <br>
                                •	En el caso de aspirantes de otras nacionalidades: Presentar la cédula de identidad que los acredite como Residentes en Venezuela (original y fotocopia ampliada).<br>
                                •	Los aspirantes nacionalizados deben presentar copia de la Gaceta Oficial con la Resolución de Nacionalización.<br>
                                •		Los aspirantes, ciudadanos venezolanos o de otras nacionalidades, que hayan realizado estudios de bachillerato fuera del territorio venezolano, deberán realizar previamente la legalización de los mismos ante las autoridades correspondientes, de acuerdo a los tratados y convenios firmados por el Estado venezolano.<br>
